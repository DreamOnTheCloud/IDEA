package Method;

public class Math数学类 {
    public static void main(String[] args) {
        //数学里的几个方法Math.max两数比大小
        int a = 5;
        int b = 4;
        System.out.println("最大值为" + Math.max(a , b));
        System.out.println("最小值为" + Math.min(a , b));
        //Math.pow求某数平方
        System.out.println(a + "的2次方为:" + Math.pow(a , 2));

    }
}
